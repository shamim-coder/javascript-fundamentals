var color = 'green';

switch (color) {
    case 'red':
        console.log('your color is red');
        break;
    case 'yellow':
        console.log('your color is yellow');
        break;
    case 'green':
        console.log('your color is green');
        break;
    default: console.log('you haven\'t select any color');
        break;
}